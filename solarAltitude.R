## demo arctic circle and tropic
library(oce)
if (!interactive())
    png("solarAltitude.png", width=7, height=4, unit="in", res=100, pointsize=9)
t0 <- as.POSIXct("2013-12-01 00:00:00", tz="UTC")
t <- seq.POSIXt(t0, by="min", length.out=24*30*60)
par(mfrow=c(2,1), mar=c(3, 3, 1, 1), mgp=c(2, 0.7, 0))
plot(t, sunAngle(t, 0, 66.55)$altitude, type='l', col='gray', ylab="")
abline(h=0)
t0 <- as.POSIXct("2013-06-01 00:00:00", tz="UTC")
t <- seq.POSIXt(t0, by="min", length.out=24*30*60)
plot(t, sunAngle(t, 0, 23.4378)$altitude, type='l', col='gray', ylab="")
abline(h=90)
if (!interactive()) dev.off()
